#include <unistd.h>

void	ft_print(char m)
{
	write(1, &m, 1);
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_print(str[i]);
		i++;
	}
}
